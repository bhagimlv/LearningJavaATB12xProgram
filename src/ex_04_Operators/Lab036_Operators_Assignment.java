public class Lab036_Operators_Assignment {
    public static void main(String[] args) {
        int age = 90;
        // Assignment operator
        // int -> data type
        // age -> identifier
        // = -> Assignment operator
        // 90 -> Literal
        System.out.println("Assignment operator equal to =");
        System.out.println("age value assigned "+age);
    }
}
