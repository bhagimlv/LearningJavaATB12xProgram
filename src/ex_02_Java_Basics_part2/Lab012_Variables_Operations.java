package ex_02_Java_Basics_part2;

public class Lab012_Variables_Operations {
    public static  void main(String args[])
    {
        int a = 10;
        System.out.println("a value is "+a);

        a = 45+23;
        System.out.println("a value is "+a);

        int age = 76;
        System.out.println("Age value is "+age);
        age = age+1;
        System.out.println("Age value is "+age);
        age = 76;
        System.out.println("Age value is "+age);




    }
}
